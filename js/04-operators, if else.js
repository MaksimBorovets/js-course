const time = 19;

if (time < 12) {
	console.log('Доброе утро');
} else if (time > 12 && time < 18) {
	console.log('Добрый день');
} else {
	console.log('Добрый вечер');
}

/*
Утро: с 6 до 12
День: с 12 до 18
Вечер: с 18 до 22
Ночь: до 6 и после 22
*/

const time = 23;

if (time > 6 && time < 12) {
	console.log('Доброе утро');
} else if (time >= 12 && time < 18) {
	console.log('Добрый день');
} else if (time >= 18 && time < 22) {
	console.log('Добрый вечер');
} else {
	console.log('Доброй ночи');
}

